    def test_pack_json_parse(self):
        """Test pack.json parsing"""
        self.logger.info("=" * 60)
        self.logger.info("Test 3: Pack JSON Parsing")
        self.logger.info("=" * 60)
        
        pack_json_path = self.inputs_dir / 'pack.json'
        if not pack_json_path.exists():
            self.logger.warning(f"pack.json not found: {pack_json_path}")
            return True  # Not a failure if file doesn't exist
        
        try:
            # Check JSON format
            with open(pack_json_path, 'r') as f:
                pack_data = json.load(f)
            
            self.logger.info("✓ pack.json JSON format is valid")
            
            # Validate structure
            required_fields = ['bin', 'action']
            valid_actions = [
                'BL1_SIGN', 'BL2_SIGN', 'PACK_BL2_BIN', 'PACK_BL2_DOWNLOAD_BIN',
                'PACK_BL1_DOWNLOAD_BIN', 'PACK_OTA_BIN', 'PACK_RAW_OTA_BIN',
                'PACK_NVS', 'STEPS_PACK_HEADER', 'USER_MFR_AES'
            ]
            
            for key, value in pack_data.items():
                if not isinstance(value, dict):
                    self.logger.error(f"✗ {key}: value must be an object")
                    return False
                
                for field in required_fields:
                    if field not in value:
                        self.logger.error(f"✗ {key}: missing required field '{field}'")
                        return False
                
                action = value.get('action')
                if action not in valid_actions:
                    self.logger.warning(f"⚠ {key}: unknown action '{action}'")
                
                bins = value.get('bin', [])
                if not isinstance(bins, list):
                    self.logger.error(f"✗ {key}: 'bin' must be a list")
                    return False
                
                self.logger.info(f"  - {key}: action={action}, bins={bins}")
            
            self.logger.info("✓ pack.json structure validation passed")
            return True
            
        except json.JSONDecodeError as e:
            self.logger.error(f"✗ pack.json JSON format error: {e}")
            return False
        except Exception as e:
            self.logger.error(f"✗ pack.json parsing failed: {e}")
            if self.debug:
                import traceback
                traceback.print_exc()
            return False
    
