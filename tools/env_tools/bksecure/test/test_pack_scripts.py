    def test_actual_pack(self):
        """
        Test actual pack process - generate pack script output files
        
        This will actually run the pack scripts to generate output files like
        all-app.bin, bootloader.bin, app_signed.bin, etc.
        """
        self.logger.info("=" * 60)
        self.logger.info("Test 7: Actual Pack Process")
        self.logger.info("=" * 60)
        
        pack_json_path = self.inputs_dir / 'pack.json'
        if not pack_json_path.exists():
            self.logger.warning(f"pack.json not found: {pack_json_path}")
            return True
        
        try:
            # Create PackJson object and run actual pack process
            pack = PackJson()
            
            # Run pack process (may require keys, but we try anyway)
            # Note: This may fail if keys are missing, but that's OK for testing
            try:
                pack.pack(
                    pack_json=str(pack_json_path),
                    img_sign_pubkey=None,
                    img_sign_privkey=None,
                    flash_aes_key=None,
                    flash_crc_en=True,
                    data_aes_key=None
                )
                self.logger.info("✓ Pack process completed")
            except Exception as pack_error:
                self.logger.warning(f"⚠ Pack process encountered errors (may need keys): {pack_error}")
                # Continue to check what files were generated anyway
            
            # Check for generated output files and move them to outputs directory
            pack_output_files = set()
            if pack_json_path.exists():
                try:
                    with open(pack_json_path, 'r') as f:
                        pack_data = json.load(f)
                    for output_file in pack_data.keys():
                        pack_output_files.add(output_file)
                except Exception as e:
                    self.logger.warning(f"Failed to parse pack.json: {e}")
            
            generated_files = []
            missing_files = []
            
            # Check for generated files in current directory (inputs_dir)
            for output_file in pack_output_files:
                file_path = Path(output_file)
                if file_path.exists():
                    # Move to outputs directory
                    if output_file in ['bootloader.bin', 'all-app.bin']:
                        dst = self.outputs_postbuild_dir / output_file
                    else:
                        dst = self.outputs_generated_dir / output_file
                    
                    try:
                        shutil.move(str(file_path), str(dst))
                        generated_files.append(output_file)
                        self.logger.info(f"  ✓ Generated and saved: {output_file} -> {dst.relative_to(self.work_dir)}")
                    except Exception as e:
                        self.logger.warning(f"  ⚠ Failed to move {output_file}: {e}")
                else:
                    missing_files.append(output_file)
            
            if generated_files:
                self.logger.info(f"✓ Generated {len(generated_files)} pack output files:")
                for f in generated_files:
                    self.logger.info(f"  - {f}")
            
            if missing_files:
                self.logger.warning(f"⚠ {len(missing_files)} pack output files not generated (may need keys or dependencies):")
                for f in missing_files:
                    self.logger.warning(f"  - {f}")
            
            # Also check for other generated files (like nvs_key.bin, nvs.bin, partition.bin)
            other_generated = ['nvs_key.bin', 'nvs.bin', 'partition.bin', 'xip_a.bin']
            for gen_file in other_generated:
                file_path = Path(gen_file)
                if file_path.exists():
                    dst = self.outputs_generated_dir / gen_file
                    try:
                        shutil.move(str(file_path), str(dst))
                        self.logger.info(f"  ✓ Found and saved: {gen_file} -> {dst.relative_to(self.work_dir)}")
                    except Exception as e:
                        self.logger.debug(f"  Failed to move {gen_file}: {e}")
            
            # Return True if at least some files were generated, or if it's just missing keys
            return len(generated_files) > 0 or len(missing_files) == len(pack_output_files)
            
        except Exception as e:
            self.logger.error(f"✗ Actual pack process failed: {e}")
            if self.debug:
                import traceback
                traceback.print_exc()
            # Don't fail the test if pack fails (may need keys)
            return True
    
    def test_binary_files_exist(self):
        """
        Test if required compiled binary files exist in inputs directory
        
        Only checks compiled binaries from bin.csv (inputs), not pack script outputs.
        Pack script outputs (from pack.json) should NOT be in inputs directory.
        """
        self.logger.info("=" * 60)
        self.logger.info("Test 6: Compiled Binary Files Check (Inputs)")
        self.logger.info("=" * 60)
        
        # Only check compiled binaries from bin.csv (these are the inputs)
        # Do NOT check pack script outputs (from pack.json)
        compiled_bins = []
        
        bin_csv_path = self.inputs_dir / 'bin.csv'
        if bin_csv_path.exists():
            try:
                bin_keys = ['Name', 'Type', 'Partition', 'Version', 'Security_counter']
                csv = Csv(str(bin_csv_path), True, bin_keys)
                for dic in csv.dic_list:
                    bin_name = dic['Name'].strip()
                    compiled_bins.append(bin_name)
            except Exception as e:
                self.logger.warning(f"Failed to parse bin.csv: {e}")
                return False
        
        if not compiled_bins:
            self.logger.warning("No compiled binaries found in bin.csv")
            return True  # Not a failure if bin.csv is empty
        
        # Get pack script output files to exclude from check
        pack_output_files = set()
        pack_json_path = self.inputs_dir / 'pack.json'
        if pack_json_path.exists():
            try:
                with open(pack_json_path, 'r') as f:
                    pack_data = json.load(f)
                for output_file in pack_data.keys():
                    pack_output_files.add(output_file)
            except Exception as e:
                self.logger.warning(f"Failed to parse pack.json: {e}")
        
        # Filter: only check compiled binaries that are NOT pack script outputs
        input_bins_to_check = [b for b in compiled_bins if b not in pack_output_files]
        
        missing_bins = []
        existing_bins = []
        
        for bin_file in input_bins_to_check:
            bin_path = self.inputs_dir / bin_file
            if bin_path.exists() or bin_path.is_symlink():
                existing_bins.append(bin_file)
            else:
                missing_bins.append(bin_file)
        
        if existing_bins:
            self.logger.info(f"✓ Found {len(existing_bins)} compiled binary files (inputs):")
            for bin_file in existing_bins:
                self.logger.info(f"  - {bin_file}")
        
        if missing_bins:
            self.logger.error(f"✗ Missing {len(missing_bins)} compiled binary files (inputs):")
            for bin_file in missing_bins:
                self.logger.error(f"  - {bin_file}")
            self.logger.error("")
            self.logger.error("These are compiled binaries (inputs) that must exist in build directory.")
            return False
        
        # Note about pack script outputs
        if pack_output_files:
            self.logger.info("")
            self.logger.info("Note: Pack script output files (from pack.json) are NOT checked here.")
            self.logger.info("They will be generated by pack scripts and saved to outputs/ directory:")
            for bin_file in sorted(pack_output_files):
                self.logger.info(f"  → {bin_file}")
        
        return True
    
    def run_tests(self, test_mode='all'):
        """
        Run tests based on test mode
        
        Args:
            test_mode: 'parse', 'validate', 'generate', 'pack', 'all'
        """
        self.logger.info("=" * 60)
        self.logger.info("Pack Scripts Test Suite")
        self.logger.info("=" * 60)
        self.logger.info(f"Project directory: {self.project_dir}")
        self.logger.info(f"Build directory: {self.build_dir}")
        self.logger.info(f"Work directory: {self.work_dir}")
        self.logger.info(f"Test mode: {test_mode}")
        self.logger.info("=" * 60)
        
        # Setup work directory
        try:
            self.setup_work_dir()
        except Exception as e:
            self.logger.error(f"Failed to setup work directory: {e}")
            return False
        
        results = {}
        
        try:
            # Run tests based on mode
            if test_mode in ['parse', 'all']:
                results['partition_parse'] = self.test_partition_parse()
            
            if test_mode in ['validate', 'all']:
                results['partition_validation'] = self.test_partition_validation()
            
            if test_mode in ['parse', 'all']:
                results['pack_json_parse'] = self.test_pack_json_parse()
            
            if test_mode in ['generate', 'all']:
                results['code_generation'] = self.test_code_generation()
            
            if test_mode in ['pack', 'all']:
                results['pack_json_object'] = self.test_pack_json_object()
            
            if test_mode in ['pack', 'all']:
                results['actual_pack'] = self.test_actual_pack()
            
            if test_mode in ['all']:
                results['binary_files'] = self.test_binary_files_exist()
            
            # Print summary
            self.logger.info("=" * 60)
            self.logger.info("Test Summary")
            self.logger.info("=" * 60)
            
            all_passed = True
            for test_name, passed in results.items():
                status = "✓ PASS" if passed else "✗ FAIL"
                self.logger.info(f"{status}: {test_name}")
                if not passed:
                    all_passed = False
            
            self.logger.info("=" * 60)
            if all_passed:
                self.logger.info("All tests passed!")
            else:
                self.logger.warning("Some tests failed. Check logs above for details.")
            
            return all_passed
            
        finally:
            self.cleanup()
        
        return False


