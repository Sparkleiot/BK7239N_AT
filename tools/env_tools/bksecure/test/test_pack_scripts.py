    def _setup_work_directory_structure(self):
        """
        Setup work directory structure and clean old files
        
        Directory structure:
        - inputs/: Configuration files and compiled binary files (input to pack scripts)
        - outputs/: Files generated by pack scripts (output from pack scripts)
            - prebuild/: Files generated by prebuild_process
            - postbuild/: Files generated by postbuild_process
            - generated/: Other generated files
        """
        # Define directory structure
        self.inputs_dir = self.work_dir / 'inputs'  # Config files + compiled binaries
        self.outputs_dir = self.work_dir / 'outputs'  # Pack script generated files
        self.outputs_prebuild_dir = self.outputs_dir / 'prebuild'
        self.outputs_postbuild_dir = self.outputs_dir / 'postbuild'
        self.outputs_generated_dir = self.outputs_dir / 'generated'
        
        # Clean old files if directory exists
        if self.work_dir.exists():
            self.logger.info(f"Cleaning old test files in: {self.work_dir}")
            try:
                shutil.rmtree(self.work_dir)
            except Exception as e:
                self.logger.warning(f"Failed to clean old files: {e}")
        
        # Create directory structure
        self.work_dir.mkdir(parents=True, exist_ok=True)
        self.inputs_dir.mkdir(parents=True, exist_ok=True)
        self.outputs_dir.mkdir(parents=True, exist_ok=True)
        self.outputs_prebuild_dir.mkdir(parents=True, exist_ok=True)
        self.outputs_postbuild_dir.mkdir(parents=True, exist_ok=True)
